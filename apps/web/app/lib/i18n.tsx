export type Locale = keyof typeof translations;
export type TranslationKey = keyof typeof translations['en'];

const EU_LANGUAGES: { code: Locale; label: string; name: string }[] = [
    { code: 'en', label: 'EN', name: 'English' },
    { code: 'tr', label: 'TR', name: 'Türkçe' },
    { code: 'de', label: 'DE', name: 'Deutsch' },
    { code: 'fr', label: 'FR', name: 'Français' },
    { code: 'es', label: 'ES', name: 'Español' },
];

// ─── CONTEXT ────────────────────────────────────
type LangCtx = {
    locale: Locale;
    setLocale: (l: Locale) => void;
    t: (key: TranslationKey) => string;
    languages: typeof EU_LANGUAGES;
};

const LanguageContext = createContext<LangCtx>({
    locale: 'en',
    setLocale: () => { },
    t: (k) => translations.en[k] as string,
    languages: EU_LANGUAGES,
});

export function LanguageProvider({ children }: { children: React.ReactNode }) {
    const [locale, setLocaleState] = useState<Locale>('en');

    // Restore from localStorage
    useEffect(() => {
        try {
            const saved = localStorage.getItem('euph_lang') as Locale;
            if (saved && translations[saved]) setLocaleState(saved);
        } catch { }
    }, []);

    const setLocale = (l: Locale) => {
        setLocaleState(l);
        try { localStorage.setItem('euph_lang', l); } catch { }
    };

    const t = (key: TranslationKey): string => {
        const dict = translations[locale] as Record<string, string>;
        return dict[key] ?? (translations.en as Record<string, string>)[key] ?? key;
    };

    return (
        <LanguageContext.Provider value={{ locale, setLocale, t, languages: EU_LANGUAGES }}>
            {children}
        </LanguageContext.Provider>
    );
}

export function useLanguage() {
    return useContext(LanguageContext);
}
